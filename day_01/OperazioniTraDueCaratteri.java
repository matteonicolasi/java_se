package day_01;

public class OperazioniTraDueCaratteri {

	public static void main(String[] args) {
		char a = 'A';
		char b = 'B';
		int somma = a+b;
		System.out.println("Somma del carattere A + B: "+somma);
		int differenza = b-a;
		System.out.println("Differenza del carattere B - A: "+differenza);
		int prodotto = a*b;
		System.out.println("Prodotto del carattere A * B: "+prodotto);
		int quoziente = b/a;
		System.out.println("Quoziente del carattere B / A: "+quoziente);
	}

}
